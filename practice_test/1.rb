# Break this code down line by line.

a = 'hello'
b = a
a = 'goodbye'

# On line 3, a is initialized as a local variable and is assigned the value of 
# string object 'hello'

# On line 4, b is initialized as a local variable and is assigned the local variable
# a which is referencing the value 'hello'

# On line 5, a is reassigned a new value with the string object 'goodbye'

# Although a is pointing to a the string object 'goodbye', b continues to reference
# or 'point to' the value 'hello'.

# This is an example of variables as pointers and assignment principles. 
